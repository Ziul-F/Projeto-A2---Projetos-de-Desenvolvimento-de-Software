public class Treinador {
    private int treinadorId;
    private String nome;
    public void setTreinadorId(int treinadorId) {
        this.treinadorId = treinadorId;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getTreinadorId() {
        return treinadorId;
    }

    public String getNome() {
        return nome;
    }

}
